
package javaprogs;

public class Dtypes {
	public static void main(String[] args) {
		byte b=20;
		short s=145;
		int i =345;
		char c='c';
		float f=23.4f;
		double d=234.4;
		System.out.println(b+" "+s+" "+i+" "+c+" "+f+" "+d);
				
		System.out.println(Byte.MIN_VALUE+" "+Byte.MAX_VALUE);
		System.out.println(Integer.MAX_VALUE+" "+Integer.MIN_VALUE);
		
		
	}
}
